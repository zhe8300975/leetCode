class Solution {
    fun leastInterval(tasks: CharArray, n: Int): Int {
        var dp=IntArray(26){0}
        var max=0
        tasks.forEach { item->
            dp[item-'A']++
            max=Math.max(max,dp[item-'A'])
        }
        var num=0
        dp.forEach { item->
            if(item==max){
                num++
            }
        }
        return Math.max(tasks.size,(max-1)*(n+1)+num)

    }
}
