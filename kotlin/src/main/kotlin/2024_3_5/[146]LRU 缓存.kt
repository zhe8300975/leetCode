    class LRUCache(val capacity: Int) {
        var head = Node(-1, -1)
        var foot = Node(-1, -1)
        var map = HashMap<Int, Node>()

        init {
            head.next = foot
            foot.pre = head
        }

        fun get(key: Int): Int {
            return map.get(key)?.also {
                it.pre?.next = it.next
                it.next?.pre = it.pre

                head.next?.pre = it
                it.next = head.next

                head.next = it
                it.pre = head
            } ?.value?:-1
        }

        fun put(key: Int, value: Int) {
            if (map.containsKey(key)) {
                map.get(key)?.let {
                    it.value = value

                    it.pre?.next = it.next
                    it.next?.pre = it.pre

                    head.next?.pre = it
                    it.next = head.next

                    head.next = it
                    it.pre = head
                }

            } else {
                if (capacity == map.size) {
                    map.remove(foot.pre?.key)
                    foot.pre?.pre?.next = foot
                    foot.pre = foot.pre?.pre
                }
                Node(key, value).also {
                    head.next?.pre = it
                    it.next = head.next

                    head.next = it
                    it.pre = head
                    map.put(key, it)
                }

            }
        }

    }

    class Node(var key: Int, var value: Int) {
        var pre: Node? = null
        var next: Node? = null
    }

    /**
